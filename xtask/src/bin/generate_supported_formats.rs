use std::fs;
use std::path::Path;

use anyhow::Result;
use provenant::golden_maintenance::run_prettier;
use provenant::parsers::metadata::ParserMetadata;

fn main() -> Result<()> {
    let args: Vec<String> = std::env::args().collect();
    let check_mode = args.get(1).map(|s| s.as_str()) == Some("--check");

    let output = generate_markdown();

    if check_mode {
        let docs_path = Path::new("docs/SUPPORTED_FORMATS.md");
        match fs::read_to_string(docs_path) {
            Ok(existing) => {
                if normalize_markdown_for_compare(&existing)
                    == normalize_markdown_for_compare(&output)
                {
                    println!("✓ docs/SUPPORTED_FORMATS.md is up to date");
                    return Ok(());
                }

                eprintln!("✗ docs/SUPPORTED_FORMATS.md is out of date");
                eprintln!(
                    "Run: cargo run --manifest-path xtask/Cargo.toml --bin generate-supported-formats"
                );
                std::process::exit(1);
            }
            Err(e) => {
                eprintln!("✗ Failed to read {}: {}", docs_path.display(), e);
                std::process::exit(1);
            }
        }
    } else {
        let docs_path = Path::new("docs/SUPPORTED_FORMATS.md");
        fs::write(docs_path, &output).expect("Failed to write docs/SUPPORTED_FORMATS.md");
        run_prettier(&[docs_path.to_path_buf()])?;
        println!("✓ Generated {}", docs_path.display());
    }

    Ok(())
}

fn generate_markdown() -> String {
    let mut parsers: Vec<&ParserMetadata> = inventory::iter::<ParserMetadata>().collect();

    parsers.sort_by(|a, b| {
        a.package_type
            .cmp(b.package_type)
            .then(a.description.cmp(b.description))
            .then(a.file_patterns.join(", ").cmp(&b.file_patterns.join(", ")))
            .then(a.primary_language.cmp(b.primary_language))
            .then(
                a.documentation_url
                    .unwrap_or("")
                    .cmp(b.documentation_url.unwrap_or("")),
            )
    });

    let mut output = String::new();
    output.push_str("# Supported Package Formats\n\n");
    output.push_str("> **⚠️ AUTO-GENERATED FILE** - Do not edit manually.\n");
    output.push_str("> This file is automatically generated from parser metadata.\n");
    output.push_str(
        "> To update, run: `cargo run --manifest-path xtask/Cargo.toml --bin generate-supported-formats`\n\n",
    );
    output.push_str("Provenant supports package formats across multiple ecosystems.\n\n");

    output.push_str("| Description | File Patterns | Package Type | Language | Documentation |\n");
    output.push_str("|-------------|---------------|--------------|----------|---------------|\n");

    for parser in &parsers {
        let description = parser.description;
        let patterns = parser.file_patterns.join(", ");
        let package_type = parser.package_type;
        let language = parser.primary_language;
        let docs = match parser.documentation_url {
            Some(url) => format!("[Link]({})", url),
            None => "-".to_string(),
        };

        output.push_str(&format!(
            "| {} | `{}` | {} | {} | {} |\n",
            description, patterns, package_type, language, docs
        ));
    }

    output
}

fn normalize_markdown_for_compare(input: &str) -> String {
    input
        .lines()
        .map(|line| {
            let trimmed = line.trim();
            if trimmed.starts_with('|') && trimmed.ends_with('|') {
                let cells: Vec<String> = trimmed
                    .trim_matches('|')
                    .split('|')
                    .map(|cell| cell.trim().to_string())
                    .collect();

                let is_separator_row = cells
                    .iter()
                    .all(|cell| !cell.is_empty() && cell.chars().all(|c| c == '-' || c == ':'));

                if is_separator_row {
                    return format!("| {} |", vec!["---"; cells.len()].join(" | "));
                }

                format!("| {} |", cells.join(" | "))
            } else {
                trimmed.to_string()
            }
        })
        .collect::<Vec<_>>()
        .join("\n")
}
